const fs = require('fs');
const path = require('path');
const PDFDocument = require('pdfkit');

const DATA_DIRS = [
  path.join(process.cwd(), 'data', 'study-material'),
  path.join(process.cwd(), 'data', 'vocab-trickfunda')
];

function generatePDF(jsonPath, outputPath, title) {
  return new Promise((resolve, reject) => {
    try {
      const data = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));
      if (!Array.isArray(data) || data.length === 0) {
        return resolve();
      }

      const doc = new PDFDocument({ margin: 50, size: 'A4' });
      const stream = fs.createWriteStream(outputPath);
      doc.pipe(stream);

      // Title
      doc.font('Helvetica-Bold').fontSize(24).text(title, { align: 'center' });
      doc.moveDown(0.5);
      doc.font('Helvetica').fontSize(12).fillColor('gray').text('Generated by TrickFunda', { align: 'center' });
      doc.moveDown(2);

      let cardIndex = 1;
      for (const card of data) {
        // Front
        doc.font('Helvetica-Bold').fontSize(16).fillColor('#3b82f6').text(`${cardIndex}. ${card.front || 'Card'}`);
        doc.moveDown(0.5);
        if (card.front_hi) {
          doc.font('Helvetica').fontSize(12).fillColor('#60a5fa').text(card.front_hi);
          doc.moveDown(0.5);
        }

        // Back / Meaning
        if (card.back) {
          doc.font('Helvetica-Bold').fontSize(12).fillColor('#4b5563').text('Meaning:');
          doc.font('Helvetica').fontSize(12).fillColor('black').text(card.back);
          doc.moveDown(0.5);
        }
        if (card.back_hi) {
          doc.font('Helvetica').fontSize(12).fillColor('black').text(card.back_hi);
          doc.moveDown(0.5);
        }

        // Details / Synonyms / Antonyms
        if (card.details) {
          doc.font('Helvetica').fontSize(11).fillColor('#374151').text(card.details);
          doc.moveDown(0.5);
        }
        if (card.synonyms && card.synonyms.length > 0) {
          doc.font('Helvetica-Bold').fontSize(11).fillColor('#10b981').text(`Synonyms: ${card.synonyms.join(', ')}`);
          doc.moveDown(0.5);
        }
        if (card.antonyms && card.antonyms.length > 0) {
          doc.font('Helvetica-Bold').fontSize(11).fillColor('#f43f5e').text(`Antonyms: ${card.antonyms.join(', ')}`);
          doc.moveDown(0.5);
        }
        if (card.example) {
          doc.font('Helvetica-Oblique').fontSize(11).fillColor('#6b7280').text(`Example: ${card.example}`);
          doc.moveDown(0.5);
        }

        // Trick / KD Hack
        const trick = card.trick || card.kdHack;
        const trickHi = card.trick_hi || card.kdHack_hi;
        if (trick || trickHi) {
          doc.moveDown(0.5);
          doc.font('Helvetica-Bold').fontSize(12).fillColor('#10b981').text('Trick / KD Hack');
          if (trick) doc.font('Helvetica').fontSize(11).fillColor('black').text(trick);
          if (trickHi) doc.font('Helvetica').fontSize(11).fillColor('black').text(trickHi);
          doc.moveDown(0.5);
        }

        doc.moveDown(1.5);
        
        // Add page if content goes too close to bottom
        if (doc.y > 700) {
          doc.addPage();
        }
        
        cardIndex++;
      }

      doc.end();

      stream.on('finish', () => resolve());
      stream.on('error', reject);
    } catch (e) {
      console.error(`Error processing ${jsonPath}:`, e);
      resolve(); // ignore bad files
    }
  });
}

async function findAndGenerate(dirPath) {
  if (!fs.existsSync(dirPath)) return;

  const entries = fs.readdirSync(dirPath, { withFileTypes: true });

  for (const entry of entries) {
    const fullPath = path.join(dirPath, entry.name);
    
    if (entry.isDirectory()) {
      await findAndGenerate(fullPath);
    } else if (entry.isFile() && entry.name.startsWith('flashcards-') && entry.name.endsWith('.json')) {
      const outputName = entry.name.replace('.json', '.pdf');
      const outputPath = path.join(dirPath, outputName);
      
      const titleMatch = entry.name.replace('flashcards-', '').replace('.json', '');
      const title = titleMatch.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ') + ' Flashcards';
      
      console.log(`Generating PDF for ${entry.name}...`);
      await generatePDF(fullPath, outputPath, title);
    }
  }
}

async function main() {
  console.log('🔨 Building Flashcard Static PDFs...');
  for (const dir of DATA_DIRS) {
    await findAndGenerate(dir);
  }
  console.log('✅ Finished building Flashcard Static PDFs!');
}

main().catch(console.error);
